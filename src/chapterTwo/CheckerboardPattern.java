package chapterTwo;

public class CheckerboardPattern {

    public static void main(String[] args) {
        for (int counter = 1; counter < 5; counter++) {
            System.out.printf("%s%n", "*  *  *  *  *  *  *  *");
            System.out.printf("%s%n", "  *  *  *  *  *  *  *  *  *");
        }
    }
}
